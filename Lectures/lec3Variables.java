public class lec3Variables {
    public static void main(String[] args) {
        // VARIABLES AND DATA TYPES
        int a = 292;
        System.out.println(a);
        float b = 2.3f;
        System.out.println(b);
        char chare = 'a';
        System.out.println(chare);
        boolean ispass = true;
        System.out.println(ispass);
        byte one = 127;
        System.out.println(one);

    }    
}
 
