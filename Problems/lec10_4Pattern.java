public class lec10_4Pattern {
    public static void main(String[] args) {
        int n = 4;
        for (int i = 1 ; i <= 4 ; i++){
            System.out.println("* * * *");
        }

        System.out.println();

        int counter = 1;
        while (counter<=n) {
            System.out.println("* * * *");
            counter++;
        }
    }    
}


